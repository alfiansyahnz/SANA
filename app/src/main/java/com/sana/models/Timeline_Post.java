package com.sana.models;

public class Timeline_Post {
    private String nameUser;
    private String waktuPost;
    private String postImg;
    private String isiPost;
    private String komentarPost;

    public String getNameUser() {
        return nameUser;
    }

    public void setNameUser(String nameUser) {
        this.nameUser = nameUser;
    }

    public String getWaktuPost() {
        return waktuPost;
    }

    public void setWaktuPost(String waktuPost) {
        this.waktuPost = waktuPost;
    }

    public String getPostImg() {
        return postImg;
    }

    public void setPostImg(String postImg) {
        this.postImg = postImg;
    }

    public String getIsiPost() {
        return isiPost;
    }

    public void setIsiPost(String isiPost) {
        this.isiPost = isiPost;
    }

    public String getKomentarPost() {
        return komentarPost;
    }

    public void setKomentarPost(String komentarPost) {
        this.komentarPost = komentarPost;
    }
}